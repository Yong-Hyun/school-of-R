### scan() 함수 사용 시 선택 사항 ###
# scan() 함수 사용 시 선택적으로 사용되는 변수의 종류 및 기능은 다음과 같다. 

# 1) 각 필드에 대한 데이터 모드를 지정(what)
# scan() 함수는 default 값으로 숫자를 입력받게 되어 있으나, 
# what 변수를 이용하여 수치 데이터가 아닌 다른 데이터의 형태를 입력할 수 있게 해준다.

# 2) 파일로부터 읽혀지는 정확한 item 들의 개수를 지정(n)
# n 변수는 입력되어지는 값들의 수를 나타낸다.

# 3) 각 레토드에 대한 필드가 한 줄인지 또는 여러 줄인지를 지정(multi.line=T)
# multi.line 변수를 이용하여 각 레코드의 데이터들이 한 줄 이상인 경우의 데이터를 읽을 수 있다.

# 4) 필요한 필드 이후의 각 줄의 모든 내용을 무시하도록 지정(flush)
# flush 변수는 여러 줄로 구성되어 있는 레코드의 일정부분, 즉 원하는 필드만을 읽고 나머지는 무시하는 명령문이다.

# 5) 필드의 분리기호(sep)
# sep 변수는 읽어야 할 데이터가 분리 기호를 포함하고 있을 때 사용한다. 
# 공백을 포함한 문자 또는 공백으로 표시된 결측치를 읽어들일 때 
# 공백과 다른 분리기호가 유용하게 사용된다.
